void main() {
  String nilai = 'D';

  switch (nilai) {
    case 'A':
      print('Sangat Baik');
      break;
    case 'B':
      print('Baik');
      break;
    case 'C':
      print('Cukup');
      break;
    case 'D':
      print('Kurang');
      break;
    case 'E':
      print('Sangat Tidak Baik');
      break;
    default:
      print('Tidak Valid');
  }
}
